package outrosTopicosJava;

public class OperacaoBitwise {
    public static void main(String[] args) {

        // Comparando o binário dos números abaixo
        int n1 = 89;
        int n2 = 60;
        System.out.println(n1 & n2);
        System.out.println(n1 | n2);
        System.out.println(n1 ^ n2);
    }
}
